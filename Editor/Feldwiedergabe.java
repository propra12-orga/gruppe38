package gruppe38.Editor;

/**
 * Verwaltet x- und y-werte
 * 
 * @author tom
 * 
 */
public class Feldwiedergabe {

	private int x;
	private int y;

	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;

	}

}
